import 'category_model.dart';

class Product {
  final int id;
  final Category category;
  final String name;
  final String image;
  final double? weight;
  final int? things;
  final Enum? weightType;
  final double rating;
  final String description;
  final double price;
  final bool isFavorite;

  Product({
    required this.id,
    required this.category,
    required this.name,
    required this.image,
    this.weight,
    this.things,
    required this.rating,
    required this.description,
    required this.price,
    required this.isFavorite,
    this.weightType,
  });
}

